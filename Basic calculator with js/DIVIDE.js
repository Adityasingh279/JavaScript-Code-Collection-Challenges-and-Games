// Function to calculate the sum using arrow function (ES6)
const divideArrow = (a, b) => {
  return a / b;
};

let divideArrow1 = (a, b) => {
  return a / b;
};

// Function to calculate the sum using traditional function declaration (ECMAScript 5.0)
function divide(a, b) {
  return a / b;
}

// Call the functions and log the results to the console
console.log(divideArrow(3, 8));
console.log(divideArrow1(3, 8));
console.log(divide(7, 9));
